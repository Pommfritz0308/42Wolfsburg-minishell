#include "libft.h"

int	ft_strupper(char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] >= 65 && s[i] <= 90)
			return (1);
		i--;
	}
	return (0);
}
